public class Fly  {
    private String depart;
    private String dest;
 
    public Fly() {
 
    }
 
    public Fly(String depart, String dest) {
        this();
        this.depart = depart;
        this.dest = dest;
    }
 
    public String getDepart() {
        return depart;
    }
 
    public String getDest() {
        return dest;
    }
}
